# Lastly, on all of the difficult parts,
# trace the values of each variable line by line, function by function.
# In fact, do another printout,
# and write in the margin the value of each variable that you need to "trace."

# Once you have a good idea of what the code does,
# go back to the computer and read it again to see if you find new things.

