# Datatypes

# Boolean
print("True or False?", True or False)  # True
print("False and True?", False and True)  # False

x = None

# Strings
print(f"None: {x}")
s = "hello"
print(s, "Jan")

# Numbers
i = 100
print("Integer:", i)
j = 10.328
print("Float or Decimal:", j)

# Lists
list = ["apple", "peach", "plumb", "orange"]
for fruit in list:
	print(fruit)

# Dictionaries (stores a key=value mapping of things.)
dictionary = {'x': 1, 'y': 2}
print(dictionary)
